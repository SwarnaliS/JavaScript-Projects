
//Addition
function addition(x,y){
    return x+y;
}

//Subtraction
function subtraction(x,y){
    return x-y;
}

//Multiplication
function multiplication(x,y){
    return x*y;
}

//multiple operations
function multiple(x,y,z,t){
    return x*y/z+t;
}

//Modulus
function modulus(x,y){
    return x%y;
}

//negation
function negation_Operator(x){
    return x;
}

